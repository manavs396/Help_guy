    private void setupLaunchButton() {
        Button btn = (Button) findViewById(R.id.English);
        btn.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Toast.makeText(MainActivity.this, "English choosed", Toast.LENGTH_SHORT)
                        .show();

                //Launching the Problem List
                Intent intent = new Intent(MainActivity.this, ProblemList.class);
                startActivity(intent);
            }

        });
    }

